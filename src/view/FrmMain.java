    private void saveToDatabase(String ime, String prezime, Zvanje zvanje) throws SQLException {
            Connection connection = null;
            String url = "jdbc:mysql://localhost:3306/projsoft";
            String user = "root";
            String pass = "";
        try {
            connection = DriverManager.getConnection(url, user, pass);
            String upit = "INSERT into nastavnik (ime,prezime,zvanje_id) values (?,?,?)";
            System.out.println(upit);
            var statement = connection.prepareStatement(upit);
            statement.setString(1, ime);
            statement.setString(2, prezime);
            statement.setInt(3, zvanje.ordinal()+1);
            int result = statement.executeUpdate();
            System.out.println(result);
            statement.close();
            connection.close();
        } catch (SQLException ex) {
            System.out.println("Konekcija nije uspesna"+url +" \n"+ex);
            connection.close();
        }
        finally{
            connection.close();
        }
            
            
    }

   

