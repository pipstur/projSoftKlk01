    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        Zvanje zvanje = (Zvanje)cbZvanje.getSelectedItem();
        boolean isValid = true;
        if (!ime.matches("^[a-zA-Z]+$") || ime.length() >= 100) {
            isValid = false;
            JOptionPane.showMessageDialog(this, "Nevalidan unos imena (preko 100 karaktera, brojevi prisutni ili prazno polje)");
        }
         if (!prezime.matches("^[a-zA-Z]+$") || prezime.length() > 100) {
            isValid = false;
            JOptionPane.showMessageDialog(this, "Nevalidan unos prezimena (preko 100 karaktera, brojevi prisutni ili prazno polje)");
        }
        if (isValid) {
            
            try {
                saveToDatabase(ime, prezime, zvanje);
                JOptionPane.showMessageDialog(this, "Podaci uspesno sacuvani u bazi!");
            } catch (SQLException ex) {
                Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

