    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        Connection connection = null;
        String url = "jdbc:mysql://localhost:3306/projsoft";
        String user = "root";
        String pass = "";
        try {
            connection = DriverManager.getConnection(url,user,pass);
            String upit = "UPDATE nastavnik SET ime = ?, prezime = ?, zvanje_id = ? WHERE id = ?";
            PreparedStatement statement = connection.prepareStatement(upit);
            statement.setString(1, txtIme.getText());
            statement.setString(2, txtPrezime.getText());
            selektovaniNastavnik.setZvanje(Zvanje.valueOf(cbZvanje.getSelectedIndex()+1));
            statement.setInt(3, selektovaniNastavnik.getZvanje().getZvanjeId());
            statement.setInt(4, selektovaniNastavnik.getId());
            int redovi = statement.executeUpdate();
            System.out.println("Uspesno apdejtovano redova: "+redovi);
            statement.close();
            connection.close();
            this.dispose();
           
        } catch (SQLException ex) {
            Logger.getLogger(FrmDetalji.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

