    private boolean obrisiProfesoraIzBaze() {
        Connection connection = null;
        String url = "jdbc:mysql://localhost:3306/projsoft";
        String user = "root";
        String pass = "";
        try {
            connection = DriverManager.getConnection(url,user,pass);
            String upit = "delete from nastavnik where id=?";
            PreparedStatement statement = connection.prepareStatement(upit);
            statement.setInt(1, selektovaniNastavnik.getId());
            statement.executeUpdate();
            statement.close();
            connection.close();
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(FrmDetalji.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
        return true;
    }
