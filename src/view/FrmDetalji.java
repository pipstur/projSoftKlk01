    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int option = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite trajno obrisati nastavnika iz sistema?", "Potvrda brisanja", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            boolean deleted = obrisiProfesoraIzBaze(); 
            if (deleted) {
                JOptionPane.showMessageDialog(this, "Nastavnik uspešno obrisan iz sistema.");
                this.dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "Brisanje nije uspelo. Molimo pokušajte ponovo.");
            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

